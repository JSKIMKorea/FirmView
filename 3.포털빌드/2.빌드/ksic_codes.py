def induty_name(code: str) -> str:
    """KSIC 5자리 코드 → 업종명. 정확 매칭 우선, 없으면 자릿수 줄여가며 검색."""
    if not code:
        return ""
    c = str(code).strip()
    for length in range(len(c), 1, -1):
        key = c[:length]
        if key in KSIC:
            return KSIC[key]
    return ""
